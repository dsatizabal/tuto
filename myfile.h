// Sum function
int sum(int x, int y) {
    return x + y;
}

// Sub function
int sub(int x, int y) {
    return x - y;
}

// Mul function
int mul(int x, int y) {
    return x * y;
}

int div(int x, int y) {
    return x/y;
}